var=False

#not operator is used to reverse the logical state of its operand. If a condition is true, then not operator will make it false and vice versa. 
print(not var) # True
print(not (5>8))

#and operator is used to combine multiple conditions. It returns true if all the conditions are true, otherwise it returns false.
print((5>3) and (3>8))

#or operator is used to combine multiple conditions. It returns true if at least one of the conditions is true, otherwise it returns false.
print((5>13) or (3>8))

